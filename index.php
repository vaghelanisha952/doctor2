    <script>
        function openPopup() {
            document.getElementById("AppointmentPopup").style.display = "flex";
        }

        function closePopup() {
            document.getElementById("AppointmentPopup").style.display = "none";
        }

        function isSunday(dateString) {
            const selected = new Date(dateString + "T00:00:00");
            return selected.getDay() === 0;
        }

        document.addEventListener("DOMContentLoaded", function () {
            const params = new URLSearchParams(window.location.search);
            if (params.get("open_appointment") === "1") {
                openPopup();

                if (window.history && window.history.replaceState) {
                    const cleanUrl = window.location.pathname;
                    window.history.replaceState({}, document.title, cleanUrl);
                }
            }
        });

        document.addEventListener("DOMContentLoaded", function () {
            const dateInput = document.getElementById("appointmentDate");
            const appointmentForm = document.querySelector("#AppointmentPopup form");
            if (!dateInput || !appointmentForm) return;

            const today = new Date();
            const yyyy = today.getFullYear();
            const mm = String(today.getMonth() + 1).padStart(2, "0");
            const dd = String(today.getDate()).padStart(2, "0");
            const minDate = `${yyyy}-${mm}-${dd}`;
            dateInput.setAttribute("min", minDate);

            dateInput.addEventListener("change", function () {
                if (!dateInput.value) return;
                if (isSunday(dateInput.value)) {
                    alert("Sunday booking allowed nahi hai. Please Monday to Saturday date select karein.");
                    dateInput.value = "";
                }
            });

            appointmentForm.addEventListener("submit", function (e) {
                if (!dateInput.value) return;
                if (isSunday(dateInput.value)) {
                    e.preventDefault();
                    alert("Sunday booking allowed nahi hai. Please Monday to Saturday date select karein.");
                    dateInput.focus();
                }
            });
        });
